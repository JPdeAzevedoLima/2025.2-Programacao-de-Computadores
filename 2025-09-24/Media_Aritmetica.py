#Desenvolva um programa em Python que solicite ao usuário dois
#valores inteiros e em seguida calcule a média aritmética entre eles.

valorA = int(input('Informe o primeiro valor: '))

valorB = int(input('Informe o segundo valor: '))

media = (valorA + valorB) / 2

print(f'A média aritmética é: {media:.1f}')